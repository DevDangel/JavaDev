public class caracteresEspeciales {
    public static void main(String[] args) {
        //Caracteres especiales
        //salto de linea \n
        var cadena1 = "Pan\nAjo";
        System.out.println(cadena1);
        //Tabulador \t
        var cadena2 = "\tBaby\tno";
        System.out.println(cadena2);
        // '\'' agrega un comilla simple
        var cadena3 = "tunometescabras \' saramambiche";
        System.out.println(cadena3);
        // '\"' agrega un comilla doble 
        var cadena4 = "Depilate \" untate";
        System.out.println(cadena4);
        //Poner un caracter barra invertida: \\
        var cadena5 = "Mi bicho es una biblia y tengo a tu puta gritanome\\amen";
        System.out.println(cadena5);

    }
    
}
